# 영어 알파벳 문자 하나를 입력받아 대소문자를 구분하는 코드를 작성하십시오.
# 입력 예1: 'A'
# 출력 예1: 대문자입니다.
# 입력 예2: 'h'
# 출력 예2: 소문자입니다.

word = input("알파벳 문자 하나 입력:")

large = ['Q','W','E','R','T','Y','U','I','O','P','A','S','D','F','G','H','J','K','L','Z','X','C','V','B','N','M']
small = ['q','w','e','r','t','y','u','i','o','p','a','s','d','f','g','h','j','k','l','z','x','c','v','b','n','m']


if word in large:
    print("대문자입니다.")
elif word in small:
    print("소문자입니다.")